+(NSString *)stringUtils:(id)stringValue
{
    NSString *string = [NSString stringWithFormat:@"%@", stringValue];
    
    if([string isEqualToString:@"<null>"] || [stringValue isKindOfClass:[NSNull class]])
    {
        string = @"";
    }
    else if(string == nil)
    {
        string = @"";
    }
    else if([string isEqualToString:@"(null)"])
    {
        string = @"";
    }
    else if ([stringValue isKindOfClass:[NSString class]]) {
        if(string.length == 0)
        {
            string = @"";
        }
    }
    
    return string;
}


